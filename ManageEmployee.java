    private void btn_searchEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_searchEmployeeActionPerformed
        // TODO add your handling code here:
        String empID = txt_empid.getText();
        if(empID.equals("")){
            JOptionPane.showMessageDialog(rootPane, "Enter Employee ID to search the employee");
        }
        else{
        try {
            DBConnection dbc = new DBConnection();
            Connection con = dbc.dbconnect();
            String str = "select * from employee where Emp_id=?";
            PreparedStatement stmt = con.prepareStatement(str);
            stmt.setString(1, txt_empid.getText());
            ResultSet rs =  stmt.executeQuery();
            if(rs.next()){
//                txt_username.setText(rs.getString("username"));
                txt_fullname.setText(rs.getString("full_name"));
                String userRoll_value = rs.getString("user_roll");
                cb_userRoll.addItem(userRoll_value);
                txt_age.setText(rs.getString("age"));
                String gender_value = rs.getString("gender");
                cb_gender.addItem(gender_value);
                txt_email.setText(rs.getString("email_id"));
                txt_phone.setText(rs.getString("phone_no"));
                txt_password.setText(rs.getString("password"));
            }
            else{
                JOptionPane.showMessageDialog(this, "NO employee found!");
            }
        } catch (SQLException ex) {
            Logger.getLogger(ManageEmployee.class.getName()).log(Level.SEVERE, null, ex);
        }
        }
    }//GEN-LAST:event_btn_searchEmployeeActionPerformed

