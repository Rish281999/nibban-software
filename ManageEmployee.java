        // TODO add your handling code here:
        String empID = txt_empid.getText();
        String fullName = txt_fullname.getText();
        String userRoll = cb_userRoll.getSelectedItem().toString();
        String age = txt_age.getText();
        String gender = cb_gender.getSelectedItem().toString();
        String emailID = txt_email.getText();
        String phoneNo = txt_phone.getText();
        String pwd = txt_password.getText();
        if(empID.equals("") || fullName.equals("") || userRoll.equals("Select") || age.equals("") || gender.equals("Select") || emailID.equals("") || phoneNo.equals("") || pwd.equals("")){
            JOptionPane.showMessageDialog(rootPane, "All feilds are required");
        }
        else{
            try {
                DBConnection dbc = new DBConnection();
                Connection con = dbc.dbconnect();
                String queryCheck = "select * from employee where Emp_id=?";
                PreparedStatement stmt = con.prepareStatement(queryCheck);
                stmt.setString(1, txt_empid.getText());
                final ResultSet rs = stmt.executeQuery();
                // if this ID already exists, we quit
                if(rs.next()) {
                    con.close();
                    JOptionPane.showMessageDialog(this, "User already exit");
                    return;
                }else{
                String str = "insert into employee values(?,?,?,?,?,?,?,?)";
                stmt = con.prepareStatement(str);
                stmt.setString(1, txt_empid.getText());
    //            stmt.setString(2, txt_username.getText());
                stmt.setString(2, txt_fullname.getText());
                String userRoll_value = cb_userRoll.getSelectedItem().toString();
                stmt.setString(3, userRoll_value);
                stmt.setString(4, txt_age.getText());
                String gender_value = cb_gender.getSelectedItem().toString();
                stmt.setString(5, gender_value);
                stmt.setString(6, txt_email.getText());
                stmt.setString(7, txt_phone.getText());
                stmt.setString(8, txt_password.getText());
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(this, "Employee Added Successfully");
                }
                txt_empid.setText("");
    //            txt_username.setText("");
                txt_fullname.setText("");
                cb_userRoll.setSelectedIndex(0);
                txt_age.setText("");
                cb_gender.setSelectedIndex(0);
                txt_email.setText("");
                txt_phone.setText("");
                txt_password.setText("");
                } 
                catch (SQLException ex) {
                    Logger.getLogger(ManageEmployee.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
